package sistemaescolarnovo;

/**
 *
 * @author aluno
 */
public class Pessoa {
    String nome;
    int idade;
    String endereco;
    String celular;

    public Pessoa(String nome, int idade, String endereco, String celular) {
        this.nome = nome;
        this.idade = idade;
        this.endereco = endereco;
        this.celular = celular;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public String getCelular() {
        return celular;
    }

    public void setCelular(String celular) {
        this.celular = celular;
    }
    
    
}
